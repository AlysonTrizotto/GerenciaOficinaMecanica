    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //Exemplo
            //String sql = "insert into cliente values('Alyson', 'alyson@gmail.com', **default**, '(41) 9 8743-8065');";
        String sql = "insert into cliente (nome, email, telefone, id)  values(?, ?, ?, default);";
        ConectaBanco();
        try{
            //Obtendo um objeto connection
            //con = DriverManager.getConnection(urlConexao, usuario, senha);
            //Criando um objeto PreparedStatement
            PreparedStatement pstmt = con.prepareStatement(sql);

            pstmt.setString(1, edtNome.getText());
            pstmt.setString(2, edtEmail.getText());
            pstmt.setString(3, edtTelefone.getText());

            int qtdRegistrosInseridos = pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Quantidade", "" + qtdRegistrosInseridos + "", JOptionPane.OK_CANCEL_OPTION);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Erro ao salvar", ex.getMessage(), JOptionPane.ERROR_MESSAGE);
        }
        finally{
            this.DesconectaBanco();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

