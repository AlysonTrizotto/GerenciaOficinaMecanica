    private void DadosBanco(){
        driverJDBC = "org.postgresql.Driver";
        usuario    = "postgres";
        senha      = "123";
        urlConexao = "jdbc:postgresql://localhost:5432/OficinaMecanica;";
    }
    
    private void ConectaBanco(){
        DadosBanco();
        try
        {
            //Passando o driver para a class responsável
            Class.forName(driverJDBC);
            //Passando os parâmentros de conexão
            con = DriverManager.getConnection(urlConexao, usuario, senha);
        }
        catch(ClassNotFoundException | SQLException ex)//Tratando erro de class inexistente e estouro de sql
        {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro ao conectar com o banco", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void DesconectaBanco(){
        try
        {
            if(!con.isClosed()){
                con.close();
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro ao desconectar do banco", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    
